var createError = require('http-errors');
var express = require('express');
var path = require('path');
var cookieParser = require('cookie-parser');
var logger = require('morgan');
var mongoose = require('mongoose');

var indexRouter = require('./src/routes/index');
var usersRouter = require('./src/routes/users');
var jobposterRouter = require("./src/routes/api/jobPoster")
var serviceProviderRouter = require("./src/routes/api/serviceProvider")
const connection = mongoose.connect('mongodb+srv://Zainmir:zainzain12@cluster0.q3goc.mongodb.net/test', { useNewUrlParser: true, useUnifiedTopology: true });
var app = express();
connection.then((db) => {
  console.log("Connected correctly to server");
}, (err) => { console.log(err); });
// view engine setup
// app.set('views', path.join(__dirname, 'views'));
// app.set('view engine', 'jade');

app.use(logger('dev'));
app.use(express.json());
app.use(function (req, res, next) {
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'GET, POST, PUT,PATCH, DELETE');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type');
  res.setHeader('Access-Control-Allow-Credentials', true);
  next();
});
app.use(express.urlencoded({ extended: false }));
app.use(cookieParser());
app.use(express.static(path.join(__dirname, 'public')));

// app.use('/', indexRouter);
app.use('/users', usersRouter);
app.use('/jobs', jobposterRouter);
app.use('/serviceProvider', serviceProviderRouter);

// catch 404 and forward to error handler
app.use(function (req, res, next) {

  next(createError(404));
});

// error handler
app.use(function (err, req, res, next) {
  // set locals, only providing error in development
  res.locals.message = err.message;
  res.locals.error = req.app.get('env') === 'development' ? err : {};

  // render the error page
  res.status(err.status || 500);
  res.render('error');
});

module.exports = app;
